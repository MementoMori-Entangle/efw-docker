function getColorData(key) {
    var colorDatas = {};
    var colorArray = [];
    var colorCnt = 0;

    switch (key) {
        case CUSTOMIZE_COLOR_KEY:
            colorArray = CUSTOMIZE_COLOR;
            break;
        case PRIMARY_COLOR_KEY:
            colorArray = PRIMARY_COLOR;
            break;
        case JAPANESE_COLOR_KEY:
            colorArray = JAPANESE_COLOR;
            break;
        case WESTERN_COLOR_KEY:
            colorArray = WESTERN_COLOR;
            break;
    }

    colorArray = removeArrayDuplicates(colorArray);
    colorCnt = colorArray.length;

    for (var i = 0; i < colorCnt; i++) {
        colorDatas[i] = colorArray[i];
    }

    return colorDatas;
}

function removeArrayDuplicates(array) {
    return array.filter(function(value, index, self) {
        return self.indexOf(value) === index;
    });
}
